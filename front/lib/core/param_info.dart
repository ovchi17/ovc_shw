class SubMetricInfo {
  final String name;
  final String description;
  final String goodRange;
  final String unit;
  const SubMetricInfo(this.name, this.description, this.goodRange, [this.unit = '']);
}

class ParamInfo {
  final String key;
  final String title;
  final String shortDescription;
  final String whatItMeasures;
  final List<SubMetricInfo> subMetricsInfo;
  final List<_ScoreAdvice> advice;

  const ParamInfo({
    required this.key,
    required this.title,
    required this.shortDescription,
    required this.whatItMeasures,
    this.subMetricsInfo = const [],
    required this.advice,
  });

  String adviceForScore(double score) {
    for (final a in advice) {
      if (score >= a.minScore && score <= a.maxScore) return a.text;
    }
    return advice.last.text;
  }
}

class _ScoreAdvice {
  final double minScore;
  final double maxScore;
  final String text;
  const _ScoreAdvice(this.minScore, this.maxScore, this.text);
}

